t_stack *init_stack_from_array(int *arr, size_t len)
{
    t_stack *stack = malloc(sizeof(t_stack));
    for (size_t i = 0; i < len; i++)
        add_front(stack, arr[i], -1);
    assign_index(stack);
    return stack;
}

